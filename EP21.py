# loop ซ้อน loop

# while loop จะเกิดขึ้นเมื่อเงื่อนไขเป็นจริง จะเข้า loop เมื่อเงื่อนไขเป็นเท็จจะกระโดดออกจาก loop ทันที

i=1

while i<=3 :  
    j=1
    while j<=5:
        print("รอบที่ =",i,"ตำแหน่งที่ j =",j)
        j+=1
    i+=1

#for ทำงานตั้งแต่ บน ลงล่าง  จนกระทั่งสิ้นสุดหมดทุกตัว

for i in range(1,4):
    print("รอบที่ =",i)
    for j in range(1,6):
        print("ตำแหน่งที่ =",j)