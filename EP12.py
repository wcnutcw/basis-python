#เขียนโปรแกรมหาตัวเลข มาก/น้อย

a=int(input("ป้อนตัวเลขที่ 1 :"))
b=int(input("ป้อนตัวเลขที่ 1 :"))

if a>b:
    print(str(a)+" มากกว่า"+str(b))  # + คือเป็นการต่อstring หรือ ใส่ , คั่นระหว่างกันก็ได้ คือไม่ต้องเขียน str()เพื่อแปลงเป็น str
elif b>a:                            
    print(b,"มากกว่า ",a)
else :
    print(a,"เท่ากับ ",b)