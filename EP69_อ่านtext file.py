# การเข้ารหัส : อ่านภาษาอื่นๆ เช่น ไทย อิ้ง และภาษาอื่นๆ ( ระบุก็ได้หรือไม่ระบุก็ได้ : อยากได้ภาษาอื่นๆนอกจากอิ้ง ก็ต้องระบุรหัส ) 

