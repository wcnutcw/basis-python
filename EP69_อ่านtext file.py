try :
    fr = open("student.txt","r",encoding = "utf-8")
    print(fr.read())   # .read()  : อ่านข้อความในไฟล์     # Hello Text File.   # .read(i)  iคือจำนวนตัวอักษรที่อยากได้ให้แสดง
    fr.close()              # .close : เมื่อดำเนินการกับตัวไฟล์เสร็จต้องปิดทุกครั้ง
except FileNotFoundError:     # FileNotFoundError : หาไฟล์ไม่เจอ
    print("หาไฟล์ไม่เจอ")
