#ลำดับความสำคัญของตัวดำเนินการทางคณิตศาสตร์ ลำดับโค้ดคือจากบนลงล่าง
# (1) is ()  วงเล็บสำคัญก่อนอันดับแรก
# (2) is เพิ่มและลดค่า -,+,--,++ จากขวาไปซ้าย
# (3) is * , / , % จากซ้ายไปขวา
# (4) is + - จากซ้ายไปขวา
# (5) is =  จาก ขวาไปซ้าย

x=5+2*10 # * ก่อนแล้วไป + 
print(x)

y= (5+2)*10/2 #วงเล็บ และจากซ้ายไปขวา คือ * ไป /  
print(y)   

z = (5+2)/10*2  #7/10
print(z)

x_1 =(5+2)/(10*2)  #7/20
print(x_1)

x_2 = 5 + 2/10*2 - 5 # 5 + 0.4 - 5 = 5.4 - 5 = 0.4
print(x_2)

