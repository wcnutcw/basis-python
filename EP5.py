# ตัวดำเนินการเปรียบเทียบ

# นำข้อมููลอย่างน้อย 2 ค่านำมาเปรียบเทียบ
# ชนิดข้อมูลเหมือนกัน
# คำตอบ 2 คำตอบ => จริง / เท็จ

x,y,z = 10,5,5
print("ค่า x > y ? :" , x>y)
print("ค่า x < y ? :" , x<y)
print("ค่า y=z ? :" , y==z)
print("ค่า x ไม่เท่ากับ y ? :" , x!=y)
print("ค่า y มากกว่าหรือเท่ากับ ค่า x? :" , y>=x)
print("ค่า y น้อยกว่าหรือเท่ากับ ค่า z? :" , y<=z)
print("ค่า x มากกว่า y และ y มากกว่า z หรือไม่ ? : " , x>y>z)